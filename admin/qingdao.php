									<span class="luoyang">青岛</span> 以古代渔村青岛得名。青岛市专名“青岛”本指城区前海一海湾内的一座小岛，因岛上绿树成荫，终年郁郁葱葱而得名“青岛”，后于明嘉靖年间首度被记载于王士性的《广志绎》中。明万历七年（1579年），即墨县令许铤主持修编的《地方事宜议·海防》中，有关青岛之名记述为：“本县东南滨海，即中国东界，望之了无津涯，惟岛屿罗峙其间。岛之可人居者，曰青、曰福、曰管……”这里的“青”，即指青岛。青岛所在的海湾因岛得名青岛湾，由此入海的一条小河也被称为青岛河。青岛河口于明万历年间建港，称青岛口；河两岸的两个村落分别得名上青岛村和下青岛村；河源头的一座山于1923年也被定名为青岛山。
